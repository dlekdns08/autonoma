#!/usr/bin/env node
/**
 * Regenerate `public/vrm/LICENSES.md` from `src/components/vtuber/vrmCatalog.json`.
 *
 * Run after adding / editing / removing a VRM entry in the catalog so
 * the in-app credits and the license file never drift.
 *
 *   npm run vrm:sync-licenses
 *
 * The script is intentionally zero-dep — plain Node + fs. It reads the
 * JSON catalog directly; no TypeScript transpile step needed.
 */

import { readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoWeb = resolve(__dirname, "..");
const catalogPath = resolve(
  repoWeb,
  "src/components/vtuber/vrmCatalog.json",
);
const outputPath = resolve(repoWeb, "public/vrm/LICENSES.md");

const catalog = JSON.parse(readFileSync(catalogPath, "utf8"));

const HEADER = `# VRM Model Licenses

All models are used under the VRoid Hub terms. Each model's individual
license conditions are listed below, as inherited from the original VRM
metadata and the creator's settings on VRoid Hub.

Credits are displayed in-app (spotlight overlay) and listed here for the
record. If you are one of the listed authors and want a credit change or
removal, please open an issue.

> This file is regenerated by \`npm run vrm:sync-licenses\` from
> \`src/components/vtuber/vrmCatalog.json\`. Do not edit by hand — edit the
> catalog instead.

---
`;

// Order of license flags in the output. Matches the VRoid Hub UI order
// so humans reading the file can skim against the source page.
const LICENSE_FIELDS = [
  ["avatarUse", "Avatar Use"],
  ["violentActs", "Violent acts"],
  ["sexualActs", "Sexual acts"],
  ["corporateUse", "Corporate use"],
  ["individualCommercialUse", "Individual commercial use"],
  ["redistribution", "Redistribution"],
  ["alterations", "Alterations"],
  ["attribution", "Attribution"],
];

function renderEntry(file, credit) {
  const title = credit.title ?? credit.character;
  const lines = [];
  lines.push(`\n## ${file}\n`);
  lines.push(`- **Character**: ${title}`);
  lines.push(`- **Author**: ${credit.author}`);
  lines.push(`- **Source**: ${credit.url}`);
  lines.push(`- **Uploaded**: ${credit.uploaded}`);
  lines.push(`- **License**:`);
  for (const [key, label] of LICENSE_FIELDS) {
    const val = credit.license?.[key] ?? "—";
    lines.push(`  - ${label}: ${val}`);
  }
  return lines.join("\n");
}

const body = Object.entries(catalog)
  .map(([file, credit]) => renderEntry(file, credit))
  .join("\n");

writeFileSync(outputPath, `${HEADER}${body}\n`, "utf8");
console.log(
  `Wrote ${outputPath} (${Object.keys(catalog).length} entries).`,
);
